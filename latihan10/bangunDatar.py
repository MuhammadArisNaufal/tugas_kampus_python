
# segitiga
def luasSegitiga(alas,tinggi):
    hasil= 0.5*alas*tinggi
    print("luas segitiga adalah:",hasil)

def kelilingSegitiga(a,b,c):
    hasil = a+b+c
    print ("keliling segitiga adalah:",hasil)

# persegi
def luasPersegi(sisi):
    hasil= sisi*sisi
    print("luas persegi adalah:",hasil)

def kelilingPersegi(sisi):
    hasil = 4*sisi
    print ("keliling persegi adalah:",hasil)

# persegi panjang
def luasPersegiPanjang(panjang,lebar):
    hasil= panjang*lebar
    print("luas persegi panjang adalah:",hasil)

def kelilingPersegiPanjang(panjang,lebar):
    hasil = 2*panjang+2*lebar
    print ("keliling persegi panjang adalah:",hasil)

# belah ketupat
def luasBelahKetupat(diagonal1,diagonal2):
    hasil= 0.5*diagonal1*diagonal2
    print("luas belah ketupat adalah:",hasil)

def kelilingBelahKetupat(sisi):
    hasil = 4*sisi
    print ("keliling belah ketupat adalah:",hasil)

# jajar genjang
def luasJajarGenjang(alas,tinggi):
    hasil= alas*tinggi
    print("luas jajar genjang adalah:",hasil)

def kelilingJajarGenjang(sisi1,sisi2):
    hasil = 2*(sisi1+sisi2)
    print ("keliling jajar genjang adalah:",hasil)

# trapesium
def luasTrapesium(sisiA,sisiB,tinggi):
    hasil = 0.5*(sisiA+sisiB)*tinggi
    print ("luas trapesium adalah:", hasil)

def kelilingTrapesium(a,b,c,d):
    hasil = a+b+c+d
    print ("keliling trapesium adalah:",hasil)

